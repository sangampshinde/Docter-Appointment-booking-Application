const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
// first parameter is is collection name = user, second parameter is schema = userSchema
const userModel = mongoose.model("user", userSchema);

module.exports = userModel;
